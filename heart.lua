R=60
C=40
F=2
n=0
PI=math.pi
sin=math.sin
cos=math.cos

cls()

function TIC()
	local n2,t,t2,x,x2,y,y2
	if n<C then
		n2=F*n
		t=2*PI*n/C
		t2=2*PI*n2/C
		x=R*cos(t)
		x2=R*cos(t2)
		y=R*sin(t)
		y2=R*sin(t2)
		line(120-x,68-y,120-x2,68-y2,5)
		print(F .. " TIMES TABLE")
		n=n+1
	end
end

